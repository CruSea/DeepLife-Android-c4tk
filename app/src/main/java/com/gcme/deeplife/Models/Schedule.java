package com.gcme.deeplife.Models;

public class Schedule {
	private String ID,Dis_Phone,Alarm_Time,Alarm_Repeat,Description;

	public Schedule() {
		super();
	}

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public String getDis_Phone() {
		return Dis_Phone;
	}

	public void setDis_Phone(String dis_Phone) {
		Dis_Phone = dis_Phone;
	}

	public String getAlarm_Time() {
		return Alarm_Time;
	}

	public void setAlarm_Time(String alarm_Time) {
		Alarm_Time = alarm_Time;
	}

	public String getAlarm_Repeat() {
		return Alarm_Repeat;
	}

	public void setAlarm_Repeat(String alarm_Repeat) {
		Alarm_Repeat = alarm_Repeat;
	}

	public String getDescription() {
		return Description;
	}

	public void setDescription(String description) {
		Description = description;
	}

	
}
